public class Piadas {

    private Long id;
    private String titulo;
    private String piada;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getPiada() {
        return piada;
    }

    public void setPiada(String piada) {
        this.piada = piada;
    }


}
